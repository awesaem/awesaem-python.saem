# **********

for y in range(0, 10, 1):
	for x in range(0, 10, 1):
		print("*", end=" ")
	print( )

# **********

for y in range(0, 10, 1):
	for x in range(y+1, 10, 1):
		print("*", end=" ")
	print( )

# **********

for y in range(10, 1, -1):
	for x in range(y-1, 1, -1):
		print("*", end=" ")
	print( )

